int main()
{
    IN(ll, H);
    IN(ll, W);
    IN(ll, M);
    A2IN(ll, h, ll, w, M);
    set<ii> S;
    vi X(W + 1), Y(H + 1);
    REP(i, M)
    {
        S.insert({h[i], w[i]});
        ++Y[h[i]];
        ++X[w[i]];
    }
    ll my = *max_element(ALL(Y));
    ll mx = *max_element(ALL(X));
    vi U, V;
    REP1(i, H)
    {
        if (Y[i] == my)
        {
            V.push_back(i);
        }
    }
    REP1(i, W)
    {
        if (X[i] == mx)
        {
            U.push_back(i);
        }
    }
    FOR(u, U)
    {
        FOR(v, V)
        {
            if (S.count({v, u}) == 0)
            {
                OUT(mx + my);
                return 0;
            }
        }
    }
    OUT(mx + my - 1);
}
